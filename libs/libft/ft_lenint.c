#include "libft.h"

int		ft_lenint(long int nb)
{
	int	resu;

	resu = 0;
	if (nb < 0)
		nb = nb * (-1);
	if (nb == 0)
		resu++;
	while (nb > 0)
	{
		nb = nb / 10;
		resu++;
	}
	return (resu);
}
